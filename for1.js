//while e for podem ser usados para a mesma intenção, exemplo:
let fer = 3
while (fer <=8){
    console.log(`O valor da variável agora é ${fer}`)
    fer++
}

for(let fel = 3; fel<=8; fel++){
    console.log(`O valor da variável está sendo ${fel}`)
}
